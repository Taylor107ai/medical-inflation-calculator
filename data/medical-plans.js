/**
 * Lookup 該 plan / variant / gender / age 嘅當前保費
 */
function getMedicalBasePremium(planId, variantId, gender, age) {
  const plan = MEDICAL_PLANS[planId];
  if (!plan) return 0;
  const variant = plan.variants.find(v => v.id === variantId);
  if (!variant) return 0;
  const arr = variant.premiums[gender];
  if (!arr) return 0;
  const idx = Math.max(0, Math.min(arr.length - 1, Math.floor(age)));
  return arr[idx];
}
